/**
 * تعبئة صفحات السياسات بمحتوى أولي — تُنفذ مرة واحدة فقط.
 */
function beauty_care_populate_policy_pages() {
	if ( get_theme_mod( 'beauty_care_policies_populated', false ) ) {
		return;
	}
	$pages = array(
		'privacy-policy'  => 'beauty_care_get_privacy_policy_content',
		'return-policy'   => 'beauty_care_get_return_policy_content',
		'shipping-policy' => 'beauty_care_get_shipping_policy_content',
	);
	$populated = 0;
	foreach ( $pages as $slug => $callback ) {
		$page = get_page_by_path( $slug );
		if ( ! $page ) {
			continue;
		}
		// لا نكتب فوق محتوى معدّل مسبقاً (أكثر من 100 حرف)
		if ( strlen( trim( $page->post_content ) ) > 100 ) {
			$populated++;
			continue;
		}
		$content = call_user_func( $callback );
		wp_update_post( array(
			'ID'           => $page->ID,
			'post_content' => trim( $content ),
		) );
		$populated++;
	}
	if ( $populated > 0 ) {
		set_theme_mod( 'beauty_care_policies_populated', 1 );
	}
}
add_action( 'after_switch_theme', 'beauty_care_populate_policy_pages' );
add_action( 'init', 'beauty_care_populate_policy_pages', 5 );
